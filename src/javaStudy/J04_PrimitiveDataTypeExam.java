package javaStudy;
/*
 * 기본형 타입 
 */
public class J04_PrimitiveDataTypeExam {
	public static void main(String[] args) {
		
		// boolean 형
		boolean onOff = false;
		System.out.println(onOff);
		
		// 문자 형
		char c = 'a'; // 문자형은 작은따옴표를 이용한다.
		
		// 정수형 int
		int n = 59;
		
		// 정수형 long
		// long형은 데이터값 끝에 항상 L을 붙여줘야 한다.
		long king = 5518510L; 
		
		// 실수형 float
		// float형은 데이터값 끝에 항상 f를 붙여줘야 한다.
		float f = 175.1f;
		
		// 실수형 double
		double d = 4119414.11141;
		
	}
}
